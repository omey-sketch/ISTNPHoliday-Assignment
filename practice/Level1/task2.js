// use a different naming convention and create a few variables 
// of your choice and store values of different data types in it.

// String datatype
let stringDataType = "Hello World";
console.log(typeof stringDataType);
// number data type
let numberDataType = 11;
console.log(typeof numberDataType);
// boolean data type
let booleanDataType = false;
console.log(typeof booleanDataType);
// big int data type
let bigIntDataType = 11n;
console.log(typeof bigIntDataType);
// null data type
let nullDataType = null;
console.log(typeof nullDataType);
// undefined data type
let undefinedDataType = undefined;
console.log(typeof undefinedDataType);
// symbol data type
let symbolDataType = Symbol("Key pair");
console.log(typeof symbolDataType);
// object data type
let snake_case={
    name : "hari",
    hair : "brown",
    work : function(){
        console.log("He will not work.");
    }
};
console.log(typeof snake_case);
